# Majority Element
# Given an array nums of size n, return the majority element.
# The majority element is the element that appears more than ⌊n / 2⌋ times where n represents the size of the array. 
# You may assume that the majority element always exists in the array.

# Example 1:
# Input: nums = [3,2,3]
# Output: 3
# Explanationn: 3 appears 2 times in the array which is greater than size of array / 2

# Example 2:
# Input: nums = [2,2,1,1,1,2,2]
# Output: 2

